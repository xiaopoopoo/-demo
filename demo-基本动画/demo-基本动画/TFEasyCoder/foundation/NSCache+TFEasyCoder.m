@implementation NSCache (TFEasyCoder)

+( NSCache *)easyCoder:(NSCacheEasyCoderBlock)block{
    return [NSObject tf_execute:[self class] back:^(id ins) {
        if (block) {
            block(( NSCache *)ins);
        }
    }];
}

-( NSCache *)easyCoder:(NSCacheEasyCoderBlock)block{
    if (block) {
        __weak typeof(self) weakSelf = self;
        block(weakSelf);
    }
    return self;
}



