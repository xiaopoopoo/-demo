@implementation UIFocusGuide (TFEasyCoder)

+( UIFocusGuide *)easyCoder:(UIFocusGuideEasyCoderBlock)block{
    return [NSObject tf_execute:[self class] back:^(id ins) {
        if (block) {
            block(( UIFocusGuide *)ins);
        }
    }];
}

-( UIFocusGuide *)easyCoder:(UIFocusGuideEasyCoderBlock)block{
    if (block) {
        __weak typeof(self) weakSelf = self;
        block(weakSelf);
    }
    return self;
}



